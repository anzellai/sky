import * as esbuild from "esbuild";
import fs from "fs";
import path from "path";
import { execSync } from "child_process";

function walkDir(dir, callback) {
  fs.readdirSync(dir).forEach(f => {
    let dirPath = path.join(dir, f);
    let isDirectory = fs.statSync(dirPath).isDirectory();
    isDirectory ? walkDir(dirPath, callback) : callback(path.join(dir, f));
  });
}

// 1. Collect Stdlib Assets
const stdlibFiles = {};
const stdlibDir = "src/stdlib";
if (fs.existsSync(stdlibDir)) {
  walkDir(stdlibDir, (filePath) => {
    if (filePath.endsWith(".sky")) {
      const relPath = path.relative("src", filePath);
      stdlibFiles[relPath] = fs.readFileSync(filePath, "utf8");
    }
  });
}

// 1b. Collect templates
const templatesDir = "templates";
if (fs.existsSync(templatesDir)) {
  walkDir(templatesDir, (filePath) => {
    stdlibFiles[filePath] = fs.readFileSync(filePath, "utf8");
  });
}

// 1c. Collect Go runtime files for Sky.Live
const runtimeDir = "src/runtime/go/skylive_rt";
if (fs.existsSync(runtimeDir)) {
  walkDir(runtimeDir, (filePath) => {
    if (filePath.endsWith(".go")) {
      const relPath = path.relative("src", filePath);
      stdlibFiles[relPath] = fs.readFileSync(filePath, "utf8");
    }
  });
}

// 2. Write assets to src/utils/assets.ts
const assetsContent = `// src/utils/assets.ts
// THIS FILE IS GENERATED BY build-binary.js. DO NOT EDIT.

export const VIRTUAL_ASSETS: Record<string, string> = ${JSON.stringify(stdlibFiles, null, 2)};

export function readVirtualAsset(path: string): string | undefined {
  return VIRTUAL_ASSETS[path];
}

export function isVirtualAsset(path: string): boolean {
  return path in VIRTUAL_ASSETS;
}
`;
fs.writeFileSync("src/utils/assets.ts", assetsContent);

// 3. Build with esbuild
const commonOptions = {
  bundle: true,
  platform: "node",
  format: "cjs",
  external: ["fsevents"],
  banner: {
    js: 'const import_meta_url = typeof document === "undefined" ? require("url").pathToFileURL(__filename).href : undefined;',
  },
  define: {
    "import.meta.url": "import_meta_url",
  }
};

esbuild.buildSync({
  ...commonOptions,
  entryPoints: ["src/bin/sky.ts"],
  outfile: "dist/sky.cjs",
});

esbuild.buildSync({
  ...commonOptions,
  entryPoints: ["src/bin/sky-lsp.ts"],
  outfile: "dist/sky-lsp.cjs",
});

// 4. Build native binaries with pkg
console.log("Packaging with pkg...");
try {
  // We use the --targets to specify platform. 
  // For simplicity, we just build for the current host platform.
  // Example: node18-macos-x64, node18-linux-x64, node18-win-x64
  const platformMap = {
    darwin: "macos",
    win32: "win",
    linux: "linux"
  };
  const archMap = {
    arm64: "arm64",
    x64: "x64"
  };
  const target = `node18-${platformMap[process.platform]}-${archMap[process.arch]}`; // You might want to detect this or build for all
  
  // pkg dist/sky.cjs --output bin/sky
  execSync(`npx pkg dist/sky.cjs --target ${target} --output bin/sky`, { stdio: "inherit" });
  execSync(`npx pkg dist/sky-lsp.cjs --target ${target} --output bin/sky-lsp`, { stdio: "inherit" });

  console.log("Native binaries created in bin/ folder.");
} catch (e) {
  console.error("pkg failed. You might need to install it: npm install -g pkg");
  console.error(e);
}
