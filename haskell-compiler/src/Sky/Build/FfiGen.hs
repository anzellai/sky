{-# LANGUAGE OverloadedStrings #-}
-- | FFI binding generator.
-- Takes the JSON output of `bin/sky-ffi-inspect <pkg>` and generates:
--   1. A Go wrapper file (ffi/<pkg>_bindings.go) that registers each function
--      under a string name via rt.Register, with panic recovery per-call.
--   2. An informational .skyi text file listing the generated binding names
--      and their Sky types for documentation. Sky programs don't need to
--      import these — they call Ffi.callPure / Ffi.callTask by name.
module Sky.Build.FfiGen
    ( generateBindings
    , runInspector
    ) where

import qualified Data.Aeson as A
import qualified Data.ByteString.Lazy as BL
import Data.List (intercalate)
import qualified Data.Text as T
import qualified Data.Text.Encoding as TE
import System.Directory (createDirectoryIfMissing)
import System.FilePath ((</>))
import System.Process (readProcessWithExitCode)


-- | Information extracted about one Go function
data FnInfo = FnInfo
    { _fnName     :: String
    , _fnParams   :: [(String, String)]  -- (name, goType)
    , _fnResults  :: [(String, String)]  -- (name, goType)
    , _fnVariadic :: Bool                -- last param is ...T
    , _fnEffect   :: String              -- "pure" | "fallible" | "effectful"
    }
    deriving (Show)


data PkgInfo = PkgInfo
    { _pkgPath   :: String
    , _pkgName   :: String
    , _pkgFns    :: [FnInfo]
    , _pkgErrors :: [String]
    }
    deriving (Show)


instance A.FromJSON FnInfo where
    parseJSON = A.withObject "FnInfo" $ \o -> FnInfo
        <$> o A..: "name"
        <*> (o A..: "params" >>= mapM parseParam)
        <*> (o A..: "results" >>= mapM parseParam)
        <*> o A..:? "variadic" A..!= False
        <*> o A..: "effect"
      where
        parseParam = A.withObject "param" $ \o -> do
            n <- o A..:? "name" A..!= ""
            t <- o A..: "type"
            return (n, t)


instance A.FromJSON PkgInfo where
    parseJSON = A.withObject "PkgInfo" $ \o -> PkgInfo
        <$> o A..: "pkg"
        <*> o A..:? "name" A..!= ""
        <*> o A..:? "functions" A..!= []
        <*> o A..:? "errors" A..!= []


-- | Run the sky-ffi-inspect helper on a package.
-- Expects to be run from the project root; the helper is at bin/sky-ffi-inspect.
-- The helper itself must be invoked from inside a Go module (cwd = sky-out),
-- which is where `go get` has added the dependency.
runInspector :: String -> IO (Either String PkgInfo)
runInspector pkgPath = do
    let cmd = "cd sky-out && ../bin/sky-ffi-inspect " ++ pkgPath
    (_, out, err) <- readProcessWithExitCode "sh" ["-c", cmd] ""
    if null out
        then return (Left $ "sky-ffi-inspect: empty output; stderr: " ++ err)
        else case A.eitherDecode (BL.fromStrict (enc out)) of
            Left e  -> return (Left $ "sky-ffi-inspect: json: " ++ e)
            Right p -> return (Right p)
  where
    enc = TE.encodeUtf8 . T.pack


-- | Generate binding files under ffi/.
-- Returns a list of binding names that users can call from Sky.
generateBindings :: PkgInfo -> IO [String]
generateBindings pkg = do
    createDirectoryIfMissing True "ffi"
    let slug = slugify (_pkgName pkg)
        goFile = "ffi" </> (slug ++ "_bindings.go")
        skyiFile = "ffi" </> (slug ++ ".skyi")
        names = map (\fn -> _pkgName pkg ++ "." ++ _fnName fn) (_pkgFns pkg)
    writeFile goFile (emitGoFile pkg)
    writeFile skyiFile (emitSkyi pkg)
    return names


-- | Emit the Go wrapper file with rt.Register calls.
-- Uses Register (effect-unknown) for every function.
emitGoFile :: PkgInfo -> String
emitGoFile pkg =
    let entries = map (emitRegister (_pkgName pkg) (_pkgPath pkg)) (_pkgFns pkg)
        anyEmitted = any (not . isSkippedEntry) entries
        pkgImport = if anyEmitted
            then "\tpkg " ++ quote (_pkgPath pkg)
            else "\t_ " ++ quote (_pkgPath pkg) ++ "  // no auto-generated bindings compile; blank import so go.mod dependency is retained"
    in unlines $
        [ "// Code generated by sky-ffi-inspect from " ++ _pkgPath pkg ++ ". DO NOT EDIT."
        , "// Re-run `sky add " ++ _pkgPath pkg ++ "` to regenerate."
        , "//"
        , "// SAFETY: every binding here is registered as effect-unknown."
        , "// Call via Sky.Ffi.callTask from Sky code — callPure will refuse."
        , "// To override a specific function as pure, audit the Go source"
        , "// then add a hand-written ffi/<pkg>_pure.go with rt.RegisterPure."
        , ""
        , "package rt"
        , ""
        , "import ("
        , pkgImport
        , "\t\"fmt\""
        , ")"
        , ""
        , "func init() {"
        ]
        ++ entries
        ++
        [ "}"
        , ""
        , "// Keep fmt referenced even if no registered function uses it."
        , "var _ = fmt.Sprintf"
        ]


-- An entry is "skipped" iff it contains no Register( call.
isSkippedEntry :: String -> Bool
isSkippedEntry s = not ("Register(" `isSubstringOf` s)


-- | Rewrite Go type names so they use the `pkg` import alias.
-- go/packages gives back fully-qualified names like
-- \"github.com/stripe/stripe-go/v82.CheckoutSession\" or
-- \"*github.com/stripe/stripe-go/v82.Event\"; we need
-- \"pkg.CheckoutSession\" and \"*pkg.Event\" in emitted code, and leave
-- qualified names from imported transitive packages (like \"time.Time\"
-- from the std lib) alone.
-- Also substitutes bare generics and nested containers ([]T, map[K]V, etc.).
normaliseType :: String -> String -> String
normaliseType pkgPath t =
    let t1 = replacePrefix (pkgPath ++ ".") "pkg." t
        t2 = replaceAnywhere ("*" ++ pkgPath ++ ".") "*pkg." t1
        t3 = replaceAnywhere ("[]" ++ pkgPath ++ ".") "[]pkg." t2
        t4 = replaceAnywhere ("[]*" ++ pkgPath ++ ".") "[]*pkg." t3
        t5 = replaceAnywhere ("map[" ++ pkgPath ++ ".") "map[pkg." t4
    in t5


-- Replace at the start of s only if it begins with pfx.
replacePrefix :: String -> String -> String -> String
replacePrefix pfx repl s
    | take (length pfx) s == pfx = repl ++ drop (length pfx) s
    | otherwise = s


-- Replace every (non-overlapping) occurrence of pfx with repl.
replaceAnywhere :: String -> String -> String -> String
replaceAnywhere pfx _    _ | null pfx = error "empty pattern"
replaceAnywhere pfx repl s = go s
  where
    n = length pfx
    go [] = []
    go xs
        | take n xs == pfx = repl ++ go (drop n xs)
        | otherwise = head xs : go (tail xs)


emitRegister :: String -> String -> FnInfo -> String
emitRegister pkgName pkgPath fn =
    let name = pkgName ++ "." ++ _fnName fn
        params = _fnParams fn
        effect = _fnEffect fn
        nArgs = length params
        -- After normalisation, any remaining `/` in a type is a transitive
        -- import path we don't have an alias for (e.g. "net/http.Client").
        -- Skip these bindings — the user would need a hand-crafted binding
        -- that imports the transitive package explicitly.
        paramsBad = any (hasExternalPkg pkgPath . snd) params
        resultsBad = any (hasExternalPkg pkgPath . snd) (_fnResults fn)
    in if paramsBad || resultsBad
        then "\t// SKIPPED " ++ name ++ " — uses transitive Go package types\n" ++
             "\t//   write a hand-crafted ffi/<file>.go binding if you need it\n"
        else unlines
            [ "\tRegister(" ++ quote name ++ ", func(args []any) any {"
            , "\t\tif len(args) < " ++ show nArgs ++ " {"
            , "\t\t\treturn fmt.Errorf(\"" ++ name ++ ": expected " ++ show nArgs ++ " args, got %d\", len(args))"
            , "\t\t}"
            , emitCall pkgPath fn
            , "\t}) // " ++ effect
            ]


-- | Should we skip this type? True when we can't emit a working binding
-- without extra imports or generic-type handling.
-- We skip:
--   - types from transitive Go modules (pkg path still present after normalise)
--   - types from Go std-lib packages we haven't explicitly imported
--     (time, io, net, etc.)
--   - single-letter types (generic type parameters)
--   - variadic-ish patterns (`[]T ...`) — Go requires f(x...) not f(x)
hasExternalPkg :: String -> String -> Bool
hasExternalPkg pkgPath t =
    let nt = normaliseType pkgPath t
    in '/' `elem` nt
       || hasStdlibPrefix nt
       || isTypeParam nt


-- Any Go type prefixed with a std-lib package name we haven't imported.
hasStdlibPrefix :: String -> Bool
hasStdlibPrefix t =
    any (`isSubstringOf` t)
        [ "time.", "io.", "net.", "http.", "url.", "sql.", "context."
        , "bufio.", "bytes.", "strings.", "regexp.", "reflect.", "os.", "fs."
        , "tls.", "x509.", "big.", "image.", "json."
        ]


-- Does `needle` appear as a qualified prefix anywhere in `hay`?
-- We only care about a `.` follow-up to avoid false-matching "bytestring".
isSubstringOf :: String -> String -> Bool
isSubstringOf needle hay = go hay
  where
    n = length needle
    go [] = False
    go xs
        | take n xs == needle = True
        | otherwise = go (tail xs)


-- Generic type parameter like "T" or "K" — any bare single uppercase letter.
-- Also detects generic type applications like "Fetch[T]" or "Iter[pkg.Foo]"
-- where a single-letter placeholder appears inside brackets.
isTypeParam :: String -> Bool
isTypeParam [c] = c >= 'A' && c <= 'Z'
isTypeParam s   = containsGenericParam s
  where
    containsGenericParam xs = case break (== '[') xs of
        (_, '[':rest) ->
            let inside = takeWhile (/= ']') rest
            in any (== ',') inside == False && isSimpleTypeParam (trim inside)
            || containsGenericParam (drop 1 rest)
        _ -> False
    trim = reverse . dropWhile (== ' ') . reverse . dropWhile (== ' ')
    isSimpleTypeParam [c] = c >= 'A' && c <= 'Z'
    isSimpleTypeParam _ = False


-- | Emit the body of the binding function.
-- Handles:
--   - zero results: returns struct{}{}
--   - one result: return it as-is
--   - (T, error): pack as SkyResult Ok/Err
--   - multi-value non-error: pack as []any
emitCall :: String -> FnInfo -> String
emitCall pkgPath fn =
    let name = _fnName fn
        params = _fnParams fn
        results = _fnResults fn
        nParams = length params
        -- Build arg expressions. For variadic functions the LAST param is
        -- reported as `[]T` by go/packages; emit it with `...` so Go splats
        -- it back into the variadic. The runtime side can pass a []any
        -- containing the items — the type assertion peels off the `[]`.
        argExprs = zipWith (\i p ->
            let normT = normaliseType pkgPath (snd p)
                cast = goArgCast i normT
                isVariadicLast = _fnVariadic fn && i == nParams - 1
            in if isVariadicLast then cast ++ "..." else cast
            ) [0..] params
        call = "pkg." ++ name ++ "(" ++ intercalate ", " argExprs ++ ")"
    in case results of
        []  -> "\t\t" ++ call ++ "\n\t\treturn struct{}{}"
        [_] -> "\t\treturn " ++ call
        _   ->
            -- Multi-result. If last result is `error`, wrap as Ok/Err.
            let lastTy = snd (last results)
                others = map snd (init results)
                bindVars = zipWith (\i _ -> "r" ++ show i) [0::Int ..] others
                allVars = bindVars ++ (if lastTy == "error" then ["err"] else ["r" ++ show (length bindVars)])
                -- Split assignment
                assignLine = "\t\t" ++ intercalate ", " allVars ++ " := " ++ call
            in if lastTy == "error"
                then unlines
                    [ assignLine
                    , "\t\tif err != nil {"
                    , "\t\t\treturn Err[any, any](err.Error())"
                    , "\t\t}"
                    , "\t\treturn Ok[any, any](" ++ packResults bindVars ++ ")"
                    ]
                else unlines
                    [ assignLine
                    , "\t\treturn []any{" ++ intercalate ", " allVars ++ "}"
                    ]


-- Pack 0/1/many values for the Ok payload in a fallible binding.
packResults :: [String] -> String
packResults []  = "struct{}{}"
packResults [v] = v
packResults vs  = "[]any{" ++ intercalate ", " vs ++ "}"


-- | Emit a Go expression that coerces args[i] to a specific Go type.
-- We use type assertion for anything that isn't a primitive Sky scalar.
-- If the assertion fails at runtime, the panic-recover in invokeFfi turns
-- the failure into an Err — no process crash.
goArgCast :: Int -> String -> String
goArgCast i t = case t of
    "string"   -> "fmt.Sprintf(\"%v\", args[" ++ show i ++ "])"
    "int"      -> "AsInt(args[" ++ show i ++ "])"
    "int8"     -> "int8(AsInt(args[" ++ show i ++ "]))"
    "int16"    -> "int16(AsInt(args[" ++ show i ++ "]))"
    "int32"    -> "int32(AsInt(args[" ++ show i ++ "]))"
    "int64"    -> "int64(AsInt(args[" ++ show i ++ "]))"
    "uint"     -> "uint(AsInt(args[" ++ show i ++ "]))"
    "uint8"    -> "uint8(AsInt(args[" ++ show i ++ "]))"
    "uint16"   -> "uint16(AsInt(args[" ++ show i ++ "]))"
    "uint32"   -> "uint32(AsInt(args[" ++ show i ++ "]))"
    "uint64"   -> "uint64(AsInt(args[" ++ show i ++ "]))"
    "float64"  -> "AsFloat(args[" ++ show i ++ "])"
    "float32"  -> "float32(AsFloat(args[" ++ show i ++ "]))"
    "bool"     -> "args[" ++ show i ++ "].(bool)"
    "byte"     -> "byte(AsInt(args[" ++ show i ++ "]))"
    "rune"     -> "rune(AsInt(args[" ++ show i ++ "]))"
    "[]byte"   ->
        -- Accept either a Go []byte already (from another FFI call) or a
        -- Sky String — canonicalise both to []byte via reflect-free check.
        "func() []byte { v := args[" ++ show i ++ "]; " ++
        "if b, ok := v.([]byte); ok { return b }; " ++
        "return []byte(fmt.Sprintf(\"%v\", v)) }()"
    "error"    -> "args[" ++ show i ++ "].(error)"
    _          -> "args[" ++ show i ++ "].(" ++ t ++ ")"


emitSkyi :: PkgInfo -> String
emitSkyi pkg = unlines $
    [ "-- Auto-generated FFI binding catalogue for " ++ _pkgPath pkg
    , "--"
    , "-- All auto-generated bindings are registered effect-unknown. Call them"
    , "-- exclusively via Sky.Ffi.callTask. Every call returns Task String a"
    , "-- with panic recovery — any Go panic is caught and surfaced as Err."
    , "--"
    , "-- Example:"
    , "--   case Task.perform (Ffi.callTask \"" ++ _pkgName pkg ++ ".<name>\" [args]) of"
    , "--       Ok v  -> ..."
    , "--       Err e -> Log.error e"
    , "--"
    , "-- To promote a specific audited function to pure, write a hand-crafted"
    , "-- ffi/<file>.go that calls rt.RegisterPure with the same name. The pure"
    , "-- registry is checked first, so it overrides the auto-generated binding."
    , "--"
    , "-- Effect classification below is a static hint only; at runtime every"
    , "-- binding goes through the same panic-recover + Task boundary."
    , ""
    , "package " ++ _pkgName pkg
    ]
    ++ map emitSkyiFn (_pkgFns pkg)


emitSkyiFn :: FnInfo -> String
emitSkyiFn fn =
    let sig = if null (_fnParams fn)
            then "() -> " ++ goResultsToSky (_fnResults fn)
            else intercalate " -> " (map (goTypeToSky . snd) (_fnParams fn))
                    ++ " -> " ++ goResultsToSky (_fnResults fn)
        -- All bindings are Task-wrapped at runtime regardless of static hint
        wrapping = "Task String"
    in "-- [" ++ _fnEffect fn ++ "] " ++ _fnName fn ++ " : " ++ sig
       ++ "   -- runtime wrap: " ++ wrapping


goResultsToSky :: [(String, String)] -> String
goResultsToSky [] = "()"
goResultsToSky [(_, t)] = goTypeToSky t
goResultsToSky rs = "(" ++ intercalate ", " (map (goTypeToSky . snd) rs) ++ ")"


goTypeToSky :: String -> String
goTypeToSky t = case t of
    "string"  -> "String"
    "int"     -> "Int"
    "int64"   -> "Int"
    "int32"   -> "Int"
    "float64" -> "Float"
    "float32" -> "Float"
    "bool"    -> "Bool"
    "error"   -> "String"   -- Go error → String error message
    _         -> stripPkg t  -- opaque pointer-ish
  where
    stripPkg = reverse . takeWhile (/= '.') . reverse


-- small helpers

quote :: String -> String
quote s = "\"" ++ concatMap esc s ++ "\""
  where
    esc '"' = "\\\""
    esc '\\' = "\\\\"
    esc c = [c]


slugify :: String -> String
slugify = map (\c -> if c `elem` ("./" :: String) then '_' else c)
