{-# LANGUAGE OverloadedStrings #-}
-- | FFI binding generator.
-- Takes the JSON output of `bin/sky-ffi-inspect <pkg>` and generates:
--   1. A Go wrapper file (ffi/<pkg>_bindings.go) that registers each function
--      under a string name via rt.Register, with panic recovery per-call.
--   2. An informational .skyi text file listing the generated binding names
--      and their Sky types for documentation. Sky programs don't need to
--      import these — they call Ffi.callPure / Ffi.callTask by name.
module Sky.Build.FfiGen
    ( generateBindings
    , runInspector
    ) where

import qualified Data.Aeson as A
import qualified Data.ByteString.Lazy as BL
import Data.List (intercalate)
import qualified Data.Text as T
import qualified Data.Text.Encoding as TE
import System.Directory (createDirectoryIfMissing)
import System.FilePath ((</>))
import System.Process (readProcessWithExitCode)


-- | Information extracted about one Go function
data FnInfo = FnInfo
    { _fnName     :: String
    , _fnParams   :: [(String, String)]  -- (name, goType)
    , _fnResults  :: [(String, String)]  -- (name, goType)
    , _fnEffect   :: String              -- "pure" | "fallible" | "effectful"
    }
    deriving (Show)


data PkgInfo = PkgInfo
    { _pkgPath   :: String
    , _pkgName   :: String
    , _pkgFns    :: [FnInfo]
    , _pkgErrors :: [String]
    }
    deriving (Show)


instance A.FromJSON FnInfo where
    parseJSON = A.withObject "FnInfo" $ \o -> FnInfo
        <$> o A..: "name"
        <*> (o A..: "params" >>= mapM parseParam)
        <*> (o A..: "results" >>= mapM parseParam)
        <*> o A..: "effect"
      where
        parseParam = A.withObject "param" $ \o -> do
            n <- o A..:? "name" A..!= ""
            t <- o A..: "type"
            return (n, t)


instance A.FromJSON PkgInfo where
    parseJSON = A.withObject "PkgInfo" $ \o -> PkgInfo
        <$> o A..: "pkg"
        <*> o A..:? "name" A..!= ""
        <*> o A..:? "functions" A..!= []
        <*> o A..:? "errors" A..!= []


-- | Run the sky-ffi-inspect helper on a package.
-- Expects to be run from the project root; the helper is at bin/sky-ffi-inspect.
-- The helper itself must be invoked from inside a Go module (cwd = sky-out),
-- which is where `go get` has added the dependency.
runInspector :: String -> IO (Either String PkgInfo)
runInspector pkgPath = do
    let cmd = "cd sky-out && ../bin/sky-ffi-inspect " ++ pkgPath
    (_, out, err) <- readProcessWithExitCode "sh" ["-c", cmd] ""
    if null out
        then return (Left $ "sky-ffi-inspect: empty output; stderr: " ++ err)
        else case A.eitherDecode (BL.fromStrict (enc out)) of
            Left e  -> return (Left $ "sky-ffi-inspect: json: " ++ e)
            Right p -> return (Right p)
  where
    enc = TE.encodeUtf8 . T.pack


-- | Generate binding files under ffi/.
-- Returns a list of binding names that users can call from Sky.
generateBindings :: PkgInfo -> IO [String]
generateBindings pkg = do
    createDirectoryIfMissing True "ffi"
    let slug = slugify (_pkgName pkg)
        goFile = "ffi" </> (slug ++ "_bindings.go")
        skyiFile = "ffi" </> (slug ++ ".skyi")
        names = map (\fn -> _pkgName pkg ++ "." ++ _fnName fn) (_pkgFns pkg)
    writeFile goFile (emitGoFile pkg)
    writeFile skyiFile (emitSkyi pkg)
    return names


-- | Emit the Go wrapper file with rt.Register calls.
-- Uses Register (effect-unknown) for every function — we cannot audit
-- arbitrary Go code for purity/panics/shared-state access. Users who
-- audit a specific function can override by writing a hand-crafted
-- binding with rt.RegisterPure in a separate ffi/*.go file, which will
-- shadow the auto-generated one (pure registry is checked first).
emitGoFile :: PkgInfo -> String
emitGoFile pkg = unlines $
    [ "// Code generated by sky-ffi-inspect from " ++ _pkgPath pkg ++ ". DO NOT EDIT."
    , "// Re-run `sky add " ++ _pkgPath pkg ++ "` to regenerate."
    , "//"
    , "// SAFETY: every binding here is registered as effect-unknown."
    , "// Call via Sky.Ffi.callTask from Sky code — callPure will refuse."
    , "// To override a specific function as pure, audit the Go source"
    , "// then add a hand-written ffi/<pkg>_pure.go with rt.RegisterPure."
    , ""
    , "package rt"
    , ""
    , "import ("
    , "\tpkg " ++ quote (_pkgPath pkg)
    , "\t\"fmt\""
    , ")"
    , ""
    , "func init() {"
    ]
    ++ map (emitRegister (_pkgName pkg)) (_pkgFns pkg)
    ++
    [ "}"
    , ""
    , "// Keep fmt referenced even if no registered function uses it."
    , "var _ = fmt.Sprintf"
    ]


emitRegister :: String -> FnInfo -> String
emitRegister pkgName fn =
    let name = pkgName ++ "." ++ _fnName fn
        params = _fnParams fn
        effect = _fnEffect fn
        nArgs = length params
    in unlines
        [ "\tRegister(" ++ quote name ++ ", func(args []any) any {"
        , "\t\tif len(args) < " ++ show nArgs ++ " {"
        , "\t\t\treturn fmt.Errorf(\"" ++ name ++ ": expected " ++ show nArgs ++ " args, got %d\", len(args))"
        , "\t\t}"
        , emitCall pkgName fn
        , "\t}) // " ++ effect
        ]


-- | Emit the body of a single binding function, including a best-effort
-- call to the Go function.
-- For types we can't trivially convert we fall through to a runtime error
-- at call time rather than a compile error at binding time.
emitCall :: String -> FnInfo -> String
emitCall pkgName fn =
    let name = _fnName fn
        params = _fnParams fn
        results = _fnResults fn
        argExprs = zipWith goArgCast [0..] (map snd params)
        call = "pkg." ++ name ++ "(" ++ intercalate ", " argExprs ++ ")"
    in case results of
        []  -> "\t\t" ++ call ++ "\n\t\treturn struct{}{}"
        [_] -> "\t\treturn " ++ call
        _   -> "\t\treturn " ++ call  -- multi-result: Go returns a tuple; the
                                       -- reflect-based caller sees it as-is;
                                       -- callers should use callFallible for (T, error)


-- | Emit a Go expression that coerces args[i] to a specific Go type.
-- Strings use fmt.Sprint; ints/floats use type-switch via rt.AsInt / AsFloat
-- already in the runtime; bools via type assertion.
goArgCast :: Int -> String -> String
goArgCast i t = case t of
    "string"  -> "fmt.Sprintf(\"%v\", args[" ++ show i ++ "])"
    "int"     -> "AsInt(args[" ++ show i ++ "])"
    "int64"   -> "int64(AsInt(args[" ++ show i ++ "]))"
    "int32"   -> "int32(AsInt(args[" ++ show i ++ "]))"
    "float64" -> "AsFloat(args[" ++ show i ++ "])"
    "bool"    -> "args[" ++ show i ++ "].(bool)"
    _         -> "args[" ++ show i ++ "]"  -- best-effort: passed as-is


emitSkyi :: PkgInfo -> String
emitSkyi pkg = unlines $
    [ "-- Auto-generated FFI binding catalogue for " ++ _pkgPath pkg
    , "--"
    , "-- All auto-generated bindings are registered effect-unknown. Call them"
    , "-- exclusively via Sky.Ffi.callTask. Every call returns Task String a"
    , "-- with panic recovery — any Go panic is caught and surfaced as Err."
    , "--"
    , "-- Example:"
    , "--   case Task.perform (Ffi.callTask \"" ++ _pkgName pkg ++ ".<name>\" [args]) of"
    , "--       Ok v  -> ..."
    , "--       Err e -> Log.error e"
    , "--"
    , "-- To promote a specific audited function to pure, write a hand-crafted"
    , "-- ffi/<file>.go that calls rt.RegisterPure with the same name. The pure"
    , "-- registry is checked first, so it overrides the auto-generated binding."
    , "--"
    , "-- Effect classification below is a static hint only; at runtime every"
    , "-- binding goes through the same panic-recover + Task boundary."
    , ""
    , "package " ++ _pkgName pkg
    ]
    ++ map emitSkyiFn (_pkgFns pkg)


emitSkyiFn :: FnInfo -> String
emitSkyiFn fn =
    let sig = if null (_fnParams fn)
            then "() -> " ++ goResultsToSky (_fnResults fn)
            else intercalate " -> " (map (goTypeToSky . snd) (_fnParams fn))
                    ++ " -> " ++ goResultsToSky (_fnResults fn)
        -- All bindings are Task-wrapped at runtime regardless of static hint
        wrapping = "Task String"
    in "-- [" ++ _fnEffect fn ++ "] " ++ _fnName fn ++ " : " ++ sig
       ++ "   -- runtime wrap: " ++ wrapping


goResultsToSky :: [(String, String)] -> String
goResultsToSky [] = "()"
goResultsToSky [(_, t)] = goTypeToSky t
goResultsToSky rs = "(" ++ intercalate ", " (map (goTypeToSky . snd) rs) ++ ")"


goTypeToSky :: String -> String
goTypeToSky t = case t of
    "string"  -> "String"
    "int"     -> "Int"
    "int64"   -> "Int"
    "int32"   -> "Int"
    "float64" -> "Float"
    "float32" -> "Float"
    "bool"    -> "Bool"
    "error"   -> "String"   -- Go error → String error message
    _         -> stripPkg t  -- opaque pointer-ish
  where
    stripPkg = reverse . takeWhile (/= '.') . reverse


-- small helpers

quote :: String -> String
quote s = "\"" ++ concatMap esc s ++ "\""
  where
    esc '"' = "\\\""
    esc '\\' = "\\\\"
    esc c = [c]


slugify :: String -> String
slugify = map (\c -> if c `elem` ("./" :: String) then '_' else c)
