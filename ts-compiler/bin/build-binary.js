import * as esbuild from "esbuild";
import fs from "fs";
import path from "path";
import { execSync } from "child_process";

function walkDir(dir, callback) {
  fs.readdirSync(dir).forEach(f => {
    let dirPath = path.join(dir, f);
    let isDirectory = fs.statSync(dirPath).isDirectory();
    isDirectory ? walkDir(dirPath, callback) : callback(path.join(dir, f));
  });
}

// 0. Resolve version: SKY_VERSION env var (CI sets this from git tag) > package.json + "-dev"
let skyVersion = process.env.SKY_VERSION || "";
if (skyVersion) {
  // CI: strip leading 'v' from tag (e.g. "v0.3.0" -> "0.3.0")
  if (skyVersion.startsWith("v")) skyVersion = skyVersion.slice(1);
} else {
  // Local build: use package.json version with "-dev" suffix
  const pkg = JSON.parse(fs.readFileSync("package.json", "utf8"));
  skyVersion = (pkg.version || "0.0.0") + "-dev";
}
console.log(`Embedding version: ${skyVersion}`);

// 1. Collect Stdlib Assets
const stdlibFiles = {};
const stdlibDir = "src/stdlib";
if (fs.existsSync(stdlibDir)) {
  walkDir(stdlibDir, (filePath) => {
    if (filePath.endsWith(".sky")) {
      const relPath = path.relative("src", filePath);
      stdlibFiles[relPath] = fs.readFileSync(filePath, "utf8");
    }
  });
}

// 1b. Collect templates
const templatesDir = "templates";
if (fs.existsSync(templatesDir)) {
  walkDir(templatesDir, (filePath) => {
    stdlibFiles[filePath] = fs.readFileSync(filePath, "utf8");
  });
}

// 1c. Collect Go runtime files for Sky.Live
const runtimeDir = "src/runtime/go/skylive_rt";
if (fs.existsSync(runtimeDir)) {
  walkDir(runtimeDir, (filePath) => {
    if (filePath.endsWith(".go")) {
      const relPath = path.relative("src", filePath);
      stdlibFiles[relPath] = fs.readFileSync(filePath, "utf8");
    }
  });
}

// 2. Write assets to src/utils/assets.ts
// Use backtick template literals to preserve unicode characters as-is.
// JSON.stringify can escape non-ASCII characters to \uXXXX sequences,
// which may not survive the pkg bundling pipeline correctly.
let assetsTs = `// src/utils/assets.ts
// THIS FILE IS GENERATED BY build-binary.js. DO NOT EDIT.

export const SKY_VERSION = ${JSON.stringify(skyVersion)};

export const VIRTUAL_ASSETS: Record<string, string> = {\n`;

for (const [key, value] of Object.entries(stdlibFiles)) {
  // Escape backticks and ${} in the file content for template literal safety
  const escaped = value
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$\{/g, '\\${');
  assetsTs += `  ${JSON.stringify(key)}: \`${escaped}\`,\n`;
}

assetsTs += `};

export function readVirtualAsset(path: string): string | undefined {
  return VIRTUAL_ASSETS[path];
}

export function isVirtualAsset(path: string): boolean {
  return path in VIRTUAL_ASSETS;
}
`;
fs.writeFileSync("src/utils/assets.ts", assetsTs);

// 3. Build with esbuild
const commonOptions = {
  bundle: true,
  platform: "node",
  format: "cjs",
  charset: "utf8",  // Preserve unicode characters as-is (don't escape to \uXXXX)
  external: ["fsevents"],
  banner: {
    js: 'const import_meta_url = typeof document === "undefined" ? require("url").pathToFileURL(__filename).href : undefined;',
  },
  define: {
    "import.meta.url": "import_meta_url",
  }
};

esbuild.buildSync({
  ...commonOptions,
  entryPoints: ["src/bin/sky.ts"],
  outfile: "dist/sky.cjs",
});

esbuild.buildSync({
  ...commonOptions,
  entryPoints: ["src/bin/sky-lsp.ts"],
  outfile: "dist/sky-lsp.cjs",
});

// 4. Build native binaries with pkg
console.log("Packaging with pkg...");
try {
  const platformMap = { darwin: "macos", win32: "win", linux: "linux" };
  const archMap = { arm64: "arm64", x64: "x64" };

  // Support cross-compilation via SKY_BUILD_TARGET env var
  // Examples: node18-macos-arm64, node18-linux-x64, node18-win-x64
  const target = process.env.SKY_BUILD_TARGET ||
    `node18-${platformMap[process.platform]}-${archMap[process.arch]}`;

  // Support custom output names via SKY_BUILD_OUTPUT env var (for CI release builds)
  const skyOutput = process.env.SKY_BUILD_OUTPUT || "bin/sky";
  const lspOutput = process.env.SKY_LSP_BUILD_OUTPUT || "bin/sky-lsp";

  execSync(`npx pkg dist/sky.cjs --target ${target} --output ${skyOutput}`, { stdio: "inherit" });
  execSync(`npx pkg dist/sky-lsp.cjs --target ${target} --output ${lspOutput}`, { stdio: "inherit" });

  console.log("Native binaries created in bin/ folder.");
} catch (e) {
  console.error("pkg failed. You might need to install it: npm install -g pkg");
  console.error(e);
}
